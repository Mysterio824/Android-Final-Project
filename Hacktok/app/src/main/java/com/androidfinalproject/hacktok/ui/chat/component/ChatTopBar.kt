@Composable
fun ChatTopBar(
    otherUser: User,
    onBackClick: () -> Unit,
    onInfoClick: () -> Unit
) {
    TopAppBar(
        title = { Text(otherUser.username) },
        navigationIcon = {
            IconButton(onClick = onBackClick) {
                Icon(Icons.Default.ArrowBack, contentDescription = "Back")
            }
        },
        actions = {
            IconButton(onClick = onInfoClick) {
                Icon(Icons.Default.MoreVert, contentDescription = "More")
            }
        }
    )
}
